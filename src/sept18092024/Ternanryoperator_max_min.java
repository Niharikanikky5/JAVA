package sept18092024;

public class Ternanryoperator_max_min {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        //  Syntax  // condition ? expression_if_true : expression_if_false
        int max = a > b ? a : b;
        int min = a < b ? a : b;
        System.out.println(max);
        System.out.println(min);
    }
}
