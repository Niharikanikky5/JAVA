package sept16092024;

public class Logicaloperators {
    public static void main(String[] Args){


                // && operator
                System.out.println((3 > 2) && (9 > 2));  // true
                System.out.println((5 > 1) && (8 < 5));  // false

                // || operator
                System.out.println((7 < 3) || (6 > 5));  // true
                System.out.println((9 > 3) || (9 < 5));  // true
                System.out.println((8 < 3) || (9 < 5));  // false

                // ! operator
                System.out.println(!(4 == 3));  // true
                System.out.println(!(6 > 3));  // false
            }
        }



